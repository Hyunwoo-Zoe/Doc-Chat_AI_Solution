
#!/bin/bash
"""stop_admin_api.sh
관리자 FastAPI 서버 종료 스크립트.

기능
----
1. 지정된 포트(기본: 8001)에서 실행 중인 FastAPI(Uvicorn) 프로세스 확인
2. PID를 찾아 종료 (kill)
3. 종료 성공 여부 확인 및 결과 출력

의도
----
- 개발/운영 환경에서 관리자 API를 안전하게 중단하기 위한 도구.
- 포트 기반으로 프로세스를 탐색하기 때문에, 여러 인스턴스 실행 시 유용함.
"""

PORT=8001

# ───────────────────────────── 종료 시도 ─────────────────────────────
echo "🛑 관리자 FastAPI 서버 종료 시도 중 (포트: $PORT)..."

# 1. 해당 포트를 LISTEN 중인 PID 조회
PID=$(lsof -t -i:$PORT)

if [ -z "$PID" ]; then
  echo "⚠️ 포트 $PORT에서 실행 중인 프로세스가 없습니다."
  exit 0
fi

# ───────────────────────────── 프로세스 종료 ─────────────────────────────
kill $PID
sleep 2

# ───────────────────────────── 종료 확인 ─────────────────────────────
if lsof -i:$PORT | grep LISTEN > /dev/null; then
  echo "❌ 종료 실패: 포트 $PORT에서 아직 프로세스가 실행 중입니다."
  exit 1
else
  echo "✅ 관리자 FastAPI 서버가 정상적으로 종료되었습니다."
fi
